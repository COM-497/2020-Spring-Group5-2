const mongoose = require('mongoose');

const OrgSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const Org = mongoose.model('Org', OrgSchema);

module.exports = Org;
